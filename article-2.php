<p> 
<b>Code Suggestion and Completion</b>
<br>
One of the greatest functions that Dreamweaver has for beginners is it’s code suggestions. Inserting and editing code helps avoid typos and quickly find the attribute needed, rather than having to look it up or use the wrong attribute. This is available for HTML, CSS, JavaScript, and PHP, meaning that tags, attribute names and values, @rules, properties, CC library colour swatches, and code snippets are hinted to developers while scripting. This allows for users to begin writing code effectively, avoid mistakes and save time.
		</p>
<p>		
<b>Live and Design View</b>
<br>
Live and Design views are two different ways of viewing the product of the user’s current code. Live view is a rendering engine that makes content appear how it would in a web browser while refreshing content whenever changes are made, this helps users to quickly check their page and also edit HTML elements at the same time.
<br>
Design view is a visual page layout that does not need to be edited by viewing the source code, although design view and code view can be split so users can utilise both the code and visual sides of their page. With Design view, users can select child tags by clicking their visual output and change them on the fly.
		</p>
<p>
<b>Code Validation</b>
<br>
Code validation is incredibly important, if there are missing or incorrect elements, it could potentially prevent the web page of displaying. Dreamweaver includes a code validator that scans the code in a document and highlights any errors. This can help developers view critical errors one-by-one and edit their mistakes.
		</p>
<p>
<b>Find and Replace</b>
<br>
The Find and Replace function is rather self-explainatory. Users are able to find code anywhere in their current script, folder, site, or open documents and replace it with updated content. This is especially useful for third-party content that may regularly update and is vital for a webpage, instead of sifting through endless lines of code on potentially multiple files, finding and replacing can be a major time saver for a developer.
<p>
<b>File Manager</b>
<br>
The File Manager section allows for developers to manage and transfer any file linked with a site, server, or local drive.
